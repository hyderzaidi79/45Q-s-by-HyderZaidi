var age = 5;
if (age <= 2) {
    console.log("Cute BABY");
}
else if (age >= 2 && age < 4) {
    console.log("Hi Toddler");
}
else if (age >= 4 && age < 13) {
    console.log("Hello Kid");
}
else if (age >= 13 && age < 20) {
    console.log("You are a TEENAGER");
}
else if (age >= 20 && age < 65) {
    console.log("ADULT");
}
else if (age > 65) {
    console.log("You are an ELDER");
}
